# Liste fournie
L = [8, 24, 27, 48, 2, 16, 9, 102, 7, 84, 91]

# Récupérer la valeur maximale, la valeur minimale et la somme
valeur_maximale = max(L)
valeur_minimale = min(L)
somme_elements = sum(L)

# Afficher les résultats
print("Liste :", L)
print("Valeur maximale :", valeur_maximale)
print("Valeur minimale :", valeur_minimale)
print("Somme des éléments :", somme_elements)
    